/**
 * gen-usage-pages.mjs — generate one "Usage examples" page per @rozie-ui
 * component, with a code-group tab per target.
 *
 * Single source of truth: the per-component `scripts/readme.mjs` already holds
 * the idiomatic per-framework consumption snippets (`USAGE`) and the
 * imperative-handle snippets (`HANDLE_USAGE`) used to render the published
 * per-leaf READMEs. This script imports those exact objects and emits a
 * VitePress page from them, so the docs can never drift from the READMEs.
 *
 * The emitted pages (`docs/components/<slug>-usage.md`) are generated artifacts
 * — do NOT edit them by hand; edit the component's `readme.mjs` and re-run.
 * Wired into `docs` `dev`/`build` so the site always regenerates them.
 */

import { existsSync, readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const HERE = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(HERE, '..', '..');
const UI_DIR = resolve(REPO_ROOT, 'packages', 'ui');
const COMPONENTS_DIR = resolve(HERE, '..', 'components');

// Emit order + tab labels. Lit's `lang` comes through as `ts` from USAGE.
const TARGETS = [
  ['react', 'React'],
  ['vue', 'Vue'],
  ['svelte', 'Svelte'],
  ['angular', 'Angular'],
  ['solid', 'Solid'],
  ['lit', 'Lit'],
];

/** Derive the component display name (e.g. `DataTable`) from the generated
 * React README's "Idiomatic **react** `Name`" line — a uniform, generated
 * source. Falls back to the slug. */
function displayNameFor(slug) {
  const readme = resolve(UI_DIR, slug, 'packages', 'react', 'README.md');
  if (existsSync(readme)) {
    const m = readFileSync(readme, 'utf8').match(/Idiomatic \*\*[a-z]+\*\* `([A-Za-z][\w]*)`/);
    if (m) return m[1];
  }
  return slug;
}

/** A `::: code-group` block from a `{ target: { lang, code } }` map. Only
 * targets present (with non-empty code) are emitted, in TARGETS order. */
/** Normalize a target's entry to a list of `{ title, lang, code }` example sets.
 * Accepts either the single-snippet shape `{ lang, code }` (most components) or
 * an aligned list of sets `[{ title, lang, code }, …]` (e.g. data-table). */
function setsFor(snippets, target) {
  const v = snippets?.[target];
  if (!v) return [];
  if (Array.isArray(v)) return v;
  return [{ title: null, lang: v.lang, code: v.code }];
}

/** The number of aligned example sets across targets. */
function setCount(snippets) {
  let n = 0;
  for (const [target] of TARGETS) n = Math.max(n, setsFor(snippets, target).length);
  return n;
}

/** The title for set `i` (first target that declares one), or null. */
function setTitle(snippets, i) {
  for (const [target] of TARGETS) {
    const s = setsFor(snippets, target)[i];
    if (s?.title) return s.title;
  }
  return null;
}

/** A `::: code-group` for set `i` — one tab per target that has that set. */
function codeGroup(snippets, i = 0) {
  const tabs = [];
  for (const [target, label] of TARGETS) {
    const snip = setsFor(snippets, target)[i];
    if (!snip?.code) continue;
    tabs.push(`\`\`\`${snip.lang} [${label}]\n${snip.code}\n\`\`\``);
  }
  if (tabs.length === 0) return null;
  return `::: code-group\n\n${tabs.join('\n\n')}\n\n:::`;
}

function relatedLinks(slug, name) {
  const links = [
    `- [${name} — showcase & API](/components/${slug}) — the full prop / event / slot / handle reference, theming, and accessibility.`,
  ];
  if (existsSync(resolve(COMPONENTS_DIR, `${slug}-comparison.md`))) {
    links.push(`- [${name} comparison](/components/${slug}-comparison) — how it stacks up against the per-framework libraries.`);
  }
  if (existsSync(resolve(COMPONENTS_DIR, `${slug}-demo.md`))) {
    links.push(`- [${name} — live demo](/components/${slug}-demo) — the real package running in the page, plus the one \`.rozie\` source and all six generated outputs.`);
  }
  return links.join('\n');
}

function renderPage(slug, name, usage, handle) {
  const nUsage = setCount(usage);
  if (nUsage === 0) throw new Error(`gen-usage-pages: no USAGE snippets for "${slug}"`);
  const handleGroup = codeGroup(handle, 0);

  const parts = [];
  parts.push('---');
  parts.push(`title: ${name} — usage examples`);
  parts.push('---');
  parts.push('');
  parts.push(`<!-- GENERATED by docs/scripts/gen-usage-pages.mjs from packages/ui/${slug}/scripts/readme.mjs — do not edit by hand. -->`);
  parts.push('');
  parts.push(`# ${name} — usage examples`);
  parts.push('');
  parts.push(
    `\`${name}\` ships as six pre-compiled, per-framework packages from a single \`.rozie\` source — install only the one for your framework (no Rozie toolchain, no build-time compile step). Each carries its engine + framework peers as **peer dependencies**, so you control their versions. The snippets below are the same idiomatic consumption code shown in each package's README; switch the tab to your framework.`,
  );
  parts.push('');
  parts.push('## Usage');
  parts.push('');
  for (let i = 0; i < nUsage; i++) {
    const group = codeGroup(usage, i);
    if (!group) continue;
    const title = setTitle(usage, i);
    if (title) {
      parts.push(`### ${title}`);
      parts.push('');
    }
    parts.push(group);
    parts.push('');
  }
  if (handleGroup) {
    parts.push('## Imperative handle');
    parts.push('');
    parts.push(
      `Beyond props and events, \`${name}\` exposes imperative methods (declared once in the \`.rozie\` source via \`$expose\`). Grab a handle through your framework's native ref mechanism and call them directly:`,
    );
    parts.push('');
    parts.push(handleGroup);
    parts.push('');
  }
  parts.push('## See also');
  parts.push('');
  parts.push(relatedLinks(slug, name));
  parts.push('');
  return parts.join('\n');
}

async function main() {
  const slugs = readdirSync(UI_DIR, { withFileTypes: true })
    .filter((d) => d.isDirectory() && existsSync(resolve(UI_DIR, d.name, 'scripts', 'readme.mjs')))
    .map((d) => d.name)
    .sort();

  const written = [];
  for (const slug of slugs) {
    const mod = pathToFileURL(resolve(UI_DIR, slug, 'scripts', 'readme.mjs')).href;
    const { USAGE, HANDLE_USAGE } = await import(mod);
    if (!USAGE) {
      throw new Error(`gen-usage-pages: ${slug}/scripts/readme.mjs does not export USAGE`);
    }
    const name = displayNameFor(slug);
    const page = renderPage(slug, name, USAGE, HANDLE_USAGE);
    const out = resolve(COMPONENTS_DIR, `${slug}-usage.md`);
    writeFileSync(out, page);
    written.push(`${slug} → ${name}`);
  }

  console.log(`[gen-usage-pages] wrote ${written.length} usage pages:`);
  for (const w of written) console.log(`  - ${w}`);
}

await main();
