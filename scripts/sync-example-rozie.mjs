/**
 * sync-example-rozie.mjs — materialize canonical `examples/<Name>.rozie` sources
 * into each consumer demo's source tree as a build-step-generated artifact.
 *
 * SINGLE SOURCE OF TRUTH: the canonical component lives at `examples/<Name>.rozie`.
 * Each consumer demo no longer commits its own copy — instead a per-demo
 * `rozie-sync.manifest.json` maps canonical component name(s) -> destination
 * path(s), and this script copies the canonical body (prefixed with an
 * AUTO-SYNCED header) into those dests. The dests are gitignored.
 *
 * TURBO-OUTPUTS CONTRACT (load-bearing): this script's writes are DECLARED
 * turbo OUTPUTS of the `sync` task (turbo.json) whose INPUTS are the canonical
 * `examples/*.rozie` + the per-demo manifest. Therefore turbo cache-replay
 * RESTORES these files on a cache hit — they are NEVER an undeclared build-hook
 * side-effect (this repo previously shipped a red main from exactly that
 * mistake: build-hook side effects must be declared outputs or cache:false).
 * `build`/`typecheck`/`test`/`dev` all `dependsOn` `sync`, so the canonical
 * sources are always materialized before any downstream tool runs.
 *
 * Pure GLUE over node:fs/node:path/node:url — no rsync, no shell — runs on
 * Windows/CI. Fail-loud: a missing canonical source or malformed manifest
 * THROWS with an actionable message and exits non-zero (mirrors codegen.mjs).
 *
 * Writes ONLY `.rozie` files — NEVER a `.ts`/`.d.rozie.ts` sidecar (a stray
 * sidecar next to a `.rozie` silently kills analogjs/ngtsc AOT). Sidecars stay
 * unplugin-generated by the demo build.
 *
 * The header is an HTML comment (NOT a `//` JS line) — a leading `//` shifts
 * `.rozie` parser byte offsets and can break source-map byte-placement checks.
 * Canonical `.rozie` files already open with an HTML-comment block, so a leading
 * HTML comment is the established, parser-safe shape.
 *
 * Usage:
 *   node scripts/sync-example-rozie.mjs --manifest <path>
 *   node scripts/sync-example-rozie.mjs <path>
 * The per-demo package.json `sync` script passes its own manifest. `dest`
 * entries are resolved relative to the MANIFEST FILE's own directory (the demo
 * root) so a manifest is portable regardless of cwd.
 */
import {
  existsSync,
  mkdirSync,
  readFileSync,
  writeFileSync,
} from 'node:fs';
import { dirname, isAbsolute, relative, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const SCRIPT_DIR = dirname(fileURLToPath(import.meta.url)); // scripts/
const REPO_ROOT = resolve(SCRIPT_DIR, '..'); // scripts/ is one level under repo root

function fail(message) {
  throw new Error(`[sync-example-rozie] ${message}`);
}

function parseArgs(argv) {
  const args = argv.slice(2);
  let manifestArg = null;
  for (let i = 0; i < args.length; i++) {
    const a = args[i];
    if (a === '--manifest') {
      manifestArg = args[i + 1] ?? null;
      i++;
    } else if (a.startsWith('--manifest=')) {
      manifestArg = a.slice('--manifest='.length);
    } else if (!a.startsWith('-') && manifestArg === null) {
      manifestArg = a;
    }
  }
  if (!manifestArg) {
    fail(
      'no manifest given. Usage: node scripts/sync-example-rozie.mjs --manifest <path>',
    );
  }
  return manifestArg;
}

function loadManifest(manifestPath) {
  const abs = isAbsolute(manifestPath)
    ? manifestPath
    : resolve(process.cwd(), manifestPath);
  if (!existsSync(abs)) {
    fail(`manifest not found: ${manifestPath} (resolved to ${abs})`);
  }
  let parsed;
  try {
    parsed = JSON.parse(readFileSync(abs, 'utf8'));
  } catch (err) {
    fail(`manifest is not valid JSON: ${abs} — ${err.message}`);
  }
  if (!parsed || !Array.isArray(parsed.entries)) {
    fail(
      `manifest must be { "entries": [ { "component", "dest" }, ... ] }: ${abs}`,
    );
  }
  return { abs, manifestDir: dirname(abs), entries: parsed.entries };
}

function syncEntry(entry, manifestDir) {
  const { component, dest } = entry;
  if (typeof component !== 'string' || !component) {
    fail(`manifest entry missing a string "component": ${JSON.stringify(entry)}`);
  }
  if (typeof dest !== 'string' || !dest) {
    fail(`manifest entry missing a string "dest": ${JSON.stringify(entry)}`);
  }
  if (/\.(d\.)?rozie\.ts$/.test(dest) || dest.endsWith('.ts')) {
    fail(
      `dest must be a .rozie file, never a .ts/.d.rozie.ts sidecar (ngtsc AOT hazard): ${dest}`,
    );
  }

  const canonical = resolve(REPO_ROOT, 'examples', `${component}.rozie`);
  if (!existsSync(canonical)) {
    fail(
      `canonical source not found: examples/${component}.rozie — manifest entry is wrong`,
    );
  }

  const body = readFileSync(canonical, 'utf8');
  const header = `<!-- AUTO-SYNCED from examples/${component}.rozie — DO NOT EDIT; edit the canonical source -->`;
  const output = `${header}\n${body}`;

  const destAbs = resolve(manifestDir, dest);
  mkdirSync(dirname(destAbs), { recursive: true });
  writeFileSync(destAbs, output, 'utf8');

  const destRel = relative(REPO_ROOT, destAbs);
  console.log(`synced: examples/${component}.rozie -> ${destRel}`);
}

function main() {
  const manifestArg = parseArgs(process.argv);
  const { entries, manifestDir } = loadManifest(manifestArg);
  if (entries.length === 0) {
    fail(`manifest has zero entries: ${manifestArg}`);
  }
  for (const entry of entries) {
    syncEntry(entry, manifestDir);
  }
}

try {
  main();
} catch (err) {
  console.error(err.message ?? String(err));
  process.exit(1);
}
